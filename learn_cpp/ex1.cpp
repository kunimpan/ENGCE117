#include <iostream>

using namespace std;

int main()
{
    float a = 2.42f;
    float b = -3.8f;
    float c = 1.58e3f;
    float d = -4.934e-6f;
    cout << "a = " << a << endl;
    cout << "b = " << b << endl;
    cout << "c = " << c << endl;
    cout << "d = " << d << endl;

    double e = 102.34;
    double f = 10.0 / 2.0;
    cout << "d + e = " << (e + f) << endl;

    return 0;
}